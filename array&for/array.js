
//INICIANDO COM ARRAY
let alunos = ["julia", "kariuxa", "xuxa"];
console.log(alunos);
   
// adicionando
alunos.push("kian");
console.log(alunos);
   
//adc num lugar definido
alunos[0] = "karina"; // quando faco isso, acabo removendo o nome anterior
console.log(alunos);
   
